package spade;

/** Holds information of an 8x8 tile in a tileset.
 *
 * @author Patrick
 */
public class SmallTile {
    
    public String index;
    public int x;
    public int y;
    
    public SmallTile(String index, int x, int y) {
        this.index = index;
        this.x = x;
        this.y = y;
    }
    
}
